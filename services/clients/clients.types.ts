// ── Lista simple de clientes para selects ──────────────────────────
export interface ClientListItem {
  id: number;
  name: string;
}

// ── Historial de precios de un cliente para un producto ────────────
export interface PriceHistoryItem {
  id: number;
  productId: number;
  productName: string;
  clientId: number;
  price: number;
  quantity: number;
  orderId: number;
  orderNumber: string;
  orderDate: string;
  currency: string;
}

// ── Cliente completo (detalle) ─────────────────────────────────────
export interface ClientDetail {
  id: number;
  name: string;
  document: string | null;
  isActive: boolean;
  createdAt: string;
  updatedAt: string;
  totalOrders: number;
  totalSpent: number;
}

// ── Filtros para GET /api/clients (paginado) ───────────────────────
export interface ClientFiltersDto {
  page?: number;
  limit?: number;
  search?: string;
  active?: boolean;
  inactive?: boolean;
}

// ── Paginación del API ─────────────────────────────────────────────
export interface ClientPagination {
  total: number;
  page: number;
  limit: number;
  totalPages: number;
  hasNextPage: boolean;
  hasPrevPage: boolean;
}

// ── Respuesta paginada del API ─────────────────────────────────────
// El http-client ya desenvuelve { success, data }, por lo que
// para endpoints paginados usamos getPaginated que retorna { data, pagination }
export interface ApiClientsResponse {
  data: ClientDetail[];
  pagination: ClientPagination;
}

// ── Respuesta ya mapeada para el frontend ──────────────────────────
export interface PaginatedClientsDto {
  items: ClientDetail[];
  total: number;
  page: number;
  limit: number;
  totalPages: number;
}

// ── DTO para crear un cliente ──────────────────────────────────────
export interface CreateClientDto {
  name: string;
  document?: string;
}

// ── DTO para actualizar un cliente ─────────────────────────────────
export interface UpdateClientDto {
  name?: string;
  document?: string;
  isActive?: boolean;
}

// ── Estadísticas de clientes ───────────────────────────────────────
export interface ClientStatistics {
  totalClients: number;
  activeClients: number;
  inactiveClients: number;
  newClientsLastMonth: number;
  totalIncome: number;
}
